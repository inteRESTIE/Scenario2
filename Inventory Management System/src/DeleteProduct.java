    public void deleteIDCombo() {
    PreparedStatement ps;
    ResultSet rs;
    String sql = "SELECT * FROM `product`";

    try {
        ps = dbConnect.getConnection().prepareStatement(sql);
        rs = ps.executeQuery();

        while (rs.next()) {
            txtID.addItem(rs.getString("ID"));
        }

        txtID.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Get the selected ID from the ComboBox
                String ID = txtID.getSelectedItem().toString();
                // Query the database to find the corresponding product name
                String productName;
                productName = getProductForID(ID); // Display the product name in the ProductNamePrev
                productPrev.setText(productName);
            }
            });
        } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }
    private String getProductForID(String selectedID) {
        String productPrev = "";
        PreparedStatement ps = null;
        ResultSet rs = null;
        String sql = "SELECT Product_Name FROM `product` WHERE ID = ?";

        try {
            ps = dbConnect.getConnection().prepareStatement(sql);
            ps.setString(1, selectedID);
            rs = ps.executeQuery();

            if (rs.next()) {
                productPrev = rs.getString("Product_Name");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close resources (PreparedStatement and ResultSet)
            try {
                if (ps != null) {
                    ps.close();
                }
                if (rs != null) {
                    rs.close();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    return productPrev;
    }

