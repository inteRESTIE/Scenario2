    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps;

        String selectedID = (String) txtID.getSelectedItem();

        try {
            String query = "DELETE FROM `product` WHERE ID = ?";
            ps = dbConnect.getConnection().prepareStatement(query);
            ps.setString(1, selectedID);

            int rowsAffected = ps.executeUpdate(); // Execute the DELETE statement

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Deleted successfully", "Deleted", JOptionPane.PLAIN_MESSAGE);
                productPrev.setText(null);
                deleteIDCombo();
                
            } else {
                JOptionPane.showMessageDialog(null, "Invalid input or ID not found", "Invalid", JOptionPane.PLAIN_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_okActionPerformed

